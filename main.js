let currentLang = 'fa';
let currentTheme = 'dark';
document.documentElement.setAttribute('data-theme', currentTheme);
document.body.classList.add('lang-fa');

function applyLang(lang) {
  currentLang = lang;
  const c = content[lang];
  document.documentElement.lang = lang;
  document.documentElement.dir = c.dir;
  document.body.className = '';
  document.body.classList.add('lang-' + lang);

  Object.keys(c).forEach(id => {
    if(['lang','dir','toast'].includes(id)) return;
    const el = document.getElementById(id);
    if (!el) return;
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      // **
    } else {
      el.innerHTML = c[id];
    }
  });

  document.querySelectorAll('[data-fa]').forEach(el => {
    if(el.id && c[el.id]) return;
    const val = el.getAttribute('data-' + lang);
    if(val) el.textContent = val;
  });

  document.getElementById('lang-toggle').textContent = lang === 'fa' ? 'EN' : 'FA';

  // marquee--
  const mq = document.getElementById('marquee');
  if(lang === 'en') {
    mq.style.animationDirection = 'normal';
  } else {
    mq.style.animationDirection = 'reverse';
  }
}

function applyTheme(theme) {
  currentTheme = theme;
  document.documentElement.setAttribute('data-theme', theme);
  document.getElementById('theme-toggle').textContent = theme === 'dark' ? '☀️' : '🌙';
}

document.getElementById('lang-toggle').addEventListener('click', () => {
  applyLang(currentLang === 'fa' ? 'en' : 'fa');
});

document.getElementById('theme-toggle').addEventListener('click', () => {
  applyTheme(currentTheme === 'dark' ? 'light' : 'dark');
});

const hamburger = document.getElementById('hamburger');
const mobileMenu = document.getElementById('mobile-menu');

hamburger.addEventListener('click', () => {
  const open = mobileMenu.classList.toggle('open');
  hamburger.textContent = open ? '✕' : '☰';
  hamburger.setAttribute('aria-expanded', open);
});

mobileMenu.querySelectorAll('a').forEach(a => {
  a.addEventListener('click', () => {
    mobileMenu.classList.remove('open');
    hamburger.textContent = '☰';
  });
});

document.querySelectorAll('.filter-btn').forEach(btn => {
  btn.addEventListener('click', () => {
    document.querySelectorAll('.filter-btn').forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
  });
});

function handleSubmit(e) {
  e.preventDefault();
  const toast = document.getElementById('toast');
  toast.textContent = content[currentLang].toast;
  toast.classList.add('show');
  document.getElementById('contact-form').reset();
  setTimeout(() => toast.classList.remove('show'), 3500);
}

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry, i) => {
    if (entry.isIntersecting) {
      setTimeout(() => {
        entry.target.classList.add('visible');
      }, Array.from(entry.target.parentElement?.querySelectorAll('.fade-up') || []).indexOf(entry.target) * 80);
      observer.unobserve(entry.target);
    }
  });
}, { threshold: 0.1, rootMargin: '0px 0px -40px 0px' });

document.querySelectorAll('.fade-up').forEach(el => observer.observe(el));

let lastScroll = 0;
const navbar = document.getElementById('navbar');
window.addEventListener('scroll', () => {
  const cs = window.scrollY;
  navbar.style.transform = (cs > lastScroll && cs > 200) ? 'translateY(-100%)' : 'translateY(0)';
  lastScroll = cs;
}, { passive: true });
navbar.style.transition = 'transform 0.3s ease, background 0.3s ease';

const track = document.getElementById('marquee');
track.innerHTML += track.innerHTML;

const isRTL = document.body.classList.contains('lang-fa');
const speed = 0.5;
let pos = 0;

function getHalfWidth() {
  return track.scrollWidth / 2;
}

function animate() {
  const halfWidth = getHalfWidth();

  if (isRTL) {
    pos -= speed;
    if (pos <= -halfWidth) pos = 0;
  } else {
    pos += speed;
    if (pos >= halfWidth) pos = 0;
  }

  track.style.transform = `translateX(${-pos}px)`;
  requestAnimationFrame(animate);
}

animate();